# Very basic operations

x = 5
y = 2
z = x + y

# add
print(f"sum of x and y is {z}")

# subtract
print(f"difference of x and y is {x-y}")

# multiply
print(f"product of x and y is {x*y}")

# divide
print(f"quotient of x and y is {x/y}")

# remainder, modulo
print(f"remainder of x and y (using modulo) is {x%y}")